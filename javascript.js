
function getComputerChoice(){
    let computerChoice = Math.floor(Math.random() * 3);
    let result;

    if(computerChoice = 1){
        result = "Rock";
    }else if(computerChoice = 2){
        result = "Paper";
    }else{
        result = "Scissors";
    }

    return result;
}

